import turtle as t
t.penup()
t.goto(-100, 0)
t.pendown()
t.begin_fill()
# прямоугольник
for i in range(2):
  t.color(237, 162, 43)
  t.fd(100)
  t.lt(90)
  t.fd(60)
  t.lt(90)
t.end_fill()
t.penup()
t.goto(50, 0)
t.pendown()
t.begin_fill()
# ромб
for i in range(2):
  t.color(237, 130, 43)
  t.lt(45+i*45)
  t.fd(50)
  t.lt(90)
  t.fd(50)
t.end_fill()
t.penup()
t.goto(-100,-100)
t.pendown()
t.lt(45)
# трапеция
t.begin_fill()
t.color(237, 62, 43)
t.fd(200)
t.lt(120)
t.fd(100)
t.lt(60)
t.fd(100)
t.lt(60)
t.fd(100)
t.end_fill()